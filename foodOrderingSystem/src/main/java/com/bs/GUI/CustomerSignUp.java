    private boolean isValidName(String name) {
        return name.matches("[a-zA-Z]+");
    }

    private boolean isValidEmail(String email) {
        return email.contains("@");
    }

    private boolean isValidTelNo(String telNo) {
        return telNo.matches("\\d{10}");
    }
    
    

