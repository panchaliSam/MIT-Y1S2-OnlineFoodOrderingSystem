    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String email = jTextField3.getText();
        String telNo = jTextField4.getText();
        String username = jTextField5.getText();
        String password = new String(jPasswordField1.getPassword());
        String password2 = new String(jPasswordField2.getPassword());
        
        
        if (fname.isEmpty() || lname.isEmpty() || email.isEmpty() || telNo.isEmpty() || username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required.");
            return;
        }

        
        if (!isValidName(fname)) {
            JOptionPane.showMessageDialog(this, "First name cannot contain numerical values.");
            jTextField1.setText(""); // Reset the JTextField
            return;
        }

        
        if (!isValidName(lname)) {
            JOptionPane.showMessageDialog(this, "Last name cannot contain numerical values.");
            jTextField2.setText(""); // Reset the JTextField
            return;
        }

        
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, "Invalid email format.");
            jTextField3.setText(""); // Reset the JTextField
            return;
        }

        
        if (!isValidTelNo(telNo)) {
            JOptionPane.showMessageDialog(this, "Telephone number should contain exactly 10 digits.");
            jTextField4.setText(""); // Reset the JTextField
            return;
        }
        
        if (!password.equals(password2)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.");
            jPasswordField1.setText(""); // Resetting the JPasswordField
            jPasswordField2.setText(""); // Resetting the JPasswordField
            return;
        }
        
        controller.addCustomer(fname, lname, email, telNo, username, password);
        JOptionPane.showMessageDialog(this, "You have sign up successfully.You can log now!");
        CustomerHomePage cushp = new CustomerHomePage();
        cushp.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

